    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
       String name = jTextField2.getText();
        String number = jTextField1.getText();

        if (name.isEmpty() || number.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int id = tableModel.getRowCount() + 1; // Assign ID based on the number of rows
        tableModel.addRow(new Object[]{id, name, number});
        JOptionPane.showMessageDialog(this, "Supplier saved successfully!");
        clearFields();
    }//GEN-LAST:event_saveActionPerformed

    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
                                    
        String searchText = jTextField3.getText();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String name = jTable1.getValueAt(i, 1).toString();
            if (name.equalsIgnoreCase(searchText)) {
                jTable1.setRowSelectionInterval(i, i);
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Supplier not found.", "Info", JOptionPane.INFORMATION_MESSAGE);    
    }//GEN-LAST:event_searchActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
         int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a supplier to update.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String name = jTextField2.getText();
        String number = jTextField1.getText();

        if (name.isEmpty() || number.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        tableModel.setValueAt(name, selectedRow, 1);
        tableModel.setValueAt(number, selectedRow, 2);
        JOptionPane.showMessageDialog(this, "Supplier updated successfully!");
        clearFields();   
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
         int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a supplier to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        tableModel.removeRow(selectedRow);
        JOptionPane.showMessageDialog(this, "Supplier deleted successfully!");
    }//GEN-LAST:event_deleteActionPerformed

