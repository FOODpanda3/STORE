    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
       String name = nametxt.getText();
        String qty = qyt.getText();
        String priceValue = price.getText();
        String supplierID = supplier.getText();

        if(name.isEmpty() || qty.isEmpty() || priceValue.isEmpty() || supplierID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        try {
            Product newProduct = new Product(name, Integer.parseInt(qty), Double.parseDouble(priceValue), supplierID);
            productList.add(newProduct);

            JOptionPane.showMessageDialog(this, "Product saved successfully!");
            refreshTable();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers for quantity and price.");
        }
    }//GEN-LAST:event_saveActionPerformed

