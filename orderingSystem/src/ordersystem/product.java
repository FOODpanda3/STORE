    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
         String searchText = searchtxt.getText();

        if(searchText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a product name to search.");
            return;
        }

        ArrayList<Product> searchResults = new ArrayList<>();
        for (Product p : productList) {
            if (p.getName().toLowerCase().contains(searchText.toLowerCase())) {
                searchResults.add(p);
            }
        }

        updateTable(searchResults);
    }//GEN-LAST:event_searchActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String name = nametxt.getText();
            String qty = qyt.getText();
            String priceValue = price.getText();
            String supplierID = supplier.getText();

            if(name.isEmpty() || qty.isEmpty() || priceValue.isEmpty() || supplierID.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill in all fields.");
                return;
            }

            try {
                Product updatedProduct = new Product(name, Integer.parseInt(qty), Double.parseDouble(priceValue), supplierID);
                productList.set(selectedRow, updatedProduct);

                JOptionPane.showMessageDialog(this, "Product updated successfully!");
                refreshTable();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Please enter valid numbers for quantity and price.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a product to update.");
        }
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            productList.remove(selectedRow);

            JOptionPane.showMessageDialog(this, "Product deleted successfully!");
            refreshTable();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a product to delete.");
        }
    }//GEN-LAST:event_deleteActionPerformed
private void refreshTable() {
        updateTable(productList);
    }
private void updateTable(ArrayList<Product> products) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Product product : products) {
            model.addRow(product.toRow());
        }
    }
public static class Product {
        private String name;
        private int quantity;
        private double price;
        private String supplierID;

        public Product(String name, int quantity, double price, String supplierID) {
            this.name = name;
            this.quantity = quantity;
            this.price = price;
            this.supplierID = supplierID;
        }

        public String getName() { return name; }
        public int getQuantity() { return quantity; }
        public double getPrice() { return price; }
        public String getSupplierID() { return supplierID; }

        public Object[] toRow() {
            return new Object[]{name, quantity, price, supplierID};
        }
    }
  
