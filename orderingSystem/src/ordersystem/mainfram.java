    public void getTax(int t) {
        if (t >= 10.0 && t <= 20.0) {
        tax = 0.5;
    } else if (t > 20.0 && t <= 30.0) {
        tax = 1.0;
    } else if (t > 30.0 && t <= 40.0) {
        tax = 1.5;
    } else if (t > 40.0 && t <= 50.0) {
        tax = 2.0;
    } else if (t > 50.0 && t <= 60.0) {
        tax = 2.5;
    } else if (t > 60.0 && t <= 100.0) {
        tax = 3.0;
    } else if (t > 100.0 && t <= 500.0) {
        tax = 4.0;
    } else if (t > 500.0 && t <= 1000.0) {
        tax = 5.0;
    }
    }

