    private void receiptbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_receiptbtnActionPerformed
    try {
        // Show the content of the receipt (text area) in a dialog for confirmation
        String receiptText = jTextArea1.getText();  // Get the current text in jTextArea1
        JOptionPane.showMessageDialog(null, receiptText, "Receipt", JOptionPane.INFORMATION_MESSAGE);
        
        // Ask for confirmation before printing
        int confirmation = JOptionPane.showConfirmDialog(null, "Do you want to print this receipt?", "Confirm Print", JOptionPane.YES_NO_OPTION);
        if (confirmation == JOptionPane.YES_OPTION) {
            // If user confirms, attempt to print
            boolean printed = jTextArea1.print();
            
            // Provide feedback on whether printing was successful
            if (printed) {
                JOptionPane.showMessageDialog(null, "Receipt Printed Successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "Printing failed or was canceled.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Receipt printing canceled.");
        }
    } catch (PrinterException ex) {
        // Handle PrinterException if printing fails
        JOptionPane.showMessageDialog(null, "Error printing receipt: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_receiptbtnActionPerformed
